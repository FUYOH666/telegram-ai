#!/usr/bin/env python3
"""–ò–∑–≤–ª–µ—á–µ–Ω–∏–µ —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω–æ–π –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ —Å —Å–∞–π—Ç–∞ scanovich.ai –¥–ª—è RAG –±–∞–∑—ã –∑–Ω–∞–Ω–∏–π."""

import re
from pathlib import Path
from typing import Dict

import httpx

BASE_URL = "https://scanovich.ai"
OUTPUT_DIR = Path(__file__).parent.parent / "knowledge_base"


def clean_text(text: str) -> str:
    """–û—á–∏—Å—Ç–∏—Ç—å —Ç–µ–∫—Å—Ç –æ—Ç –ª–∏—à–Ω–∏—Ö –ø—Ä–æ–±–µ–ª–æ–≤ –∏ –ø–µ—Ä–µ–Ω–æ—Å–æ–≤."""
    if not text:
        return ""
    # –£–¥–∞–ª—è–µ–º –º–Ω–æ–∂–µ—Å—Ç–≤–µ–Ω–Ω—ã–µ –ø—Ä–æ–±–µ–ª—ã –∏ –ø–µ—Ä–µ–Ω–æ—Å—ã
    text = re.sub(r"\s+", " ", text)
    text = re.sub(r"\n\s*\n", "\n\n", text)
    return text.strip()


def extract_section_content(html: str, section_id: str) -> str:
    """–ò–∑–≤–ª–µ—á—å —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ —Å–µ–∫—Ü–∏–∏ –ø–æ ID –∏—Å–ø–æ–ª—å–∑—É—è —Ä–µ–≥—É–ª—è—Ä–Ω—ã–µ –≤—ã—Ä–∞–∂–µ–Ω–∏—è."""
    # –ò—â–µ–º —Å–µ–∫—Ü–∏—é –ø–æ id
    pattern = rf'<[^>]*id=["\']?{re.escape(section_id)}["\']?[^>]*>.*?</section>'
    match = re.search(pattern, html, re.DOTALL | re.IGNORECASE)
    
    if not match:
        return ""
    
    section_html = match.group(0)
    
    # –£–¥–∞–ª—è–µ–º —Å–∫—Ä–∏–ø—Ç—ã –∏ —Å—Ç–∏–ª–∏
    section_html = re.sub(r"<script[^>]*>.*?</script>", "", section_html, flags=re.DOTALL | re.IGNORECASE)
    section_html = re.sub(r"<style[^>]*>.*?</style>", "", section_html, flags=re.DOTALL | re.IGNORECASE)
    
    # –ò–∑–≤–ª–µ–∫–∞–µ–º —Ç–µ–∫—Å—Ç –∏–∑ HTML —Ç–µ–≥–æ–≤
    text = re.sub(r"<[^>]+>", " ", section_html)
    text = re.sub(r"\s+", " ", text)
    
    return clean_text(text)


def extract_website_content() -> Dict[str, str]:
    """–ò–∑–≤–ª–µ—á—å –≤–µ—Å—å –∫–æ–Ω—Ç–µ–Ω—Ç —Å —Å–∞–π—Ç–∞."""
    print(f"–ó–∞–≥—Ä—É–∑–∫–∞ –∫–æ–Ω—Ç–µ–Ω—Ç–∞ —Å {BASE_URL}...")
    
    try:
        response = httpx.get(BASE_URL, timeout=30, follow_redirects=True)
        response.raise_for_status()
        
        html = response.text
        
        # –ò–∑–≤–ª–µ–∫–∞–µ–º –º–µ—Ç–∞-–∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é
        title_match = re.search(r"<title[^>]*>(.*?)</title>", html, re.IGNORECASE | re.DOTALL)
        title_text = title_match.group(1).strip() if title_match else ""
        
        desc_match = re.search(r'<meta[^>]*name=["\']description["\'][^>]*content=["\']([^"\']+)["\']', html, re.IGNORECASE)
        description = desc_match.group(1) if desc_match else ""
        
        # –ò–∑–≤–ª–µ–∫–∞–µ–º –æ—Å–Ω–æ–≤–Ω—ã–µ —Å–µ–∫—Ü–∏–∏
        content = {
            "hero": "",
            "why_choose": "",
            "how_we_work": "",
            "competencies": "",
            "industries": "",
            "about": "",
            "contact": "",
        }
        
        # Hero —Å–µ–∫—Ü–∏—è - –∏—â–µ–º h1 –∏ –ø–µ—Ä–≤—ã–µ –ø–∞—Ä–∞–≥—Ä–∞—Ñ—ã
        h1_match = re.search(r"<h1[^>]*>(.*?)</h1>", html, re.IGNORECASE | re.DOTALL)
        if h1_match:
            h1_text = re.sub(r"<[^>]+>", "", h1_match.group(1)).strip()
            content["hero"] = h1_text
        
        # –ò–∑–≤–ª–µ–∫–∞–µ–º –ø–∞—Ä–∞–≥—Ä–∞—Ñ—ã –∏–∑ hero —Å–µ–∫—Ü–∏–∏
        p_matches = re.findall(r"<p[^>]*>(.*?)</p>", html, re.IGNORECASE | re.DOTALL)[:3]
        for p_match in p_matches:
            p_text = re.sub(r"<[^>]+>", "", p_match).strip()
            if p_text and len(p_text) > 20:
                content["hero"] += "\n\n" + p_text
        
        # –ü–æ—á–µ–º—É –≤—ã–±–∏—Ä–∞—é—Ç –Ω–∞—Å
        content["why_choose"] = extract_section_content(html, "why-choose")
        
        # –ö–∞–∫ –º—ã —Ä–∞–±–æ—Ç–∞–µ–º
        content["how_we_work"] = extract_section_content(html, "how-we-work")
        
        # –ö–æ–º–ø–µ—Ç–µ–Ω—Ü–∏–∏
        content["competencies"] = extract_section_content(html, "competencies")
        
        # –ò–Ω–¥—É—Å—Ç—Ä–∏–∏
        content["industries"] = extract_section_content(html, "industries")
        
        # –û –∫–æ–º–ø–∞–Ω–∏–∏ - –∏—â–µ–º –ø–æ —Ç–µ–∫—Å—Ç—É
        about_pattern = r"(?:–û –∫–æ–º–ø–∞–Ω–∏–∏|–ù–∞—à–∞ –º–∏—Å—Å–∏—è)[^<]*<[^>]*>.*?</section>"
        about_match = re.search(about_pattern, html, re.IGNORECASE | re.DOTALL)
        if about_match:
            about_html = about_match.group(0)
            about_html = re.sub(r"<script[^>]*>.*?</script>", "", about_html, flags=re.DOTALL | re.IGNORECASE)
            about_html = re.sub(r"<style[^>]*>.*?</style>", "", about_html, flags=re.DOTALL | re.IGNORECASE)
            content["about"] = re.sub(r"<[^>]+>", " ", about_html)
            content["about"] = re.sub(r"\s+", " ", content["about"]).strip()
        
        # –ö–æ–Ω—Ç–∞–∫—Ç—ã
        content["contact"] = extract_section_content(html, "contact")
        
        return {
            "title": title_text,
            "description": description,
            **content,
        }
        
    except Exception as e:
        print(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ —Å–∞–π—Ç–∞: {e}")
        return {}


